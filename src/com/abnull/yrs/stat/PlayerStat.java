package com.abnull.yrs.stat;

public class PlayerStat {

	public int str = 0;
	public int def = 0;
	public int acc = 0;
	public int agi = 0;
	
	public PlayerStat()
	{
		
	}
	
	public PlayerStat(int str, int def, int acc, int agi)
	{
		this.str = str;
		this.def = def;
		this.acc = acc;
		this.agi = agi;
	}
	
}
